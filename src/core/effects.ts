import { z } from "zod";
import { GameState } from "./state.js";
import { GameEvent } from "./events.js";
import { PureRand } from "./rng.js";
import { calculateTradePrice, checkReputationTrade, getMerchantGold } from "./economy.js";

export const EffectSchema = z.union([
  z.object({ set_flag: z.string() }),
  z.object({ clear_flag: z.string() }),
  z.object({ add_item: z.string() }),
  z.object({ remove_item: z.string() }),
  z.object({
    set_var: z.object({
      name: z.string(),
      value: z.number(),
    }),
  }),
  z.object({
    inc_var: z.object({
      name: z.string(),
      by: z.number(),
    }),
  }),
  z.object({
    dec_var: z.object({
      name: z.string(),
      by: z.number(),
    }),
  }),
  z.object({ add_journal: z.string() }),
  z.object({ goto: z.string() }),
  z.object({
    unlock_exit: z.object({
      from: z.string(),
      to: z.string(),
    }),
  }),
  z.object({ open_object: z.string() }),
  z.object({
    set_object_locked: z.object({
      id: z.string(),
      locked: z.boolean(),
    }),
  }),
  z.object({ narrate: z.string() }),
  z.object({ end_game: z.string() }),
  z.object({
    roll_skill_check: z.object({
      skill: z.string(),
      difficulty: z.number(),
      success_flag: z.string(),
    }),
  }),
  z.object({
    start_combat: z.object({
      npc_id: z.string(),
    }),
  }),
  z.object({
    damage_player: z.object({
      by: z.number(),
      msg: z.string().optional(),
    }),
  }),
  z.object({
    heal_player: z.object({
      by: z.number(),
      msg: z.string().optional(),
    }),
  }),
  z.object({
    generate_procedural_room: z.object({
      direction: z.string(),
      to_id: z.string(),
      name: z.string().optional(),
      description: z.string().optional(),
      template_id: z.string().optional(),
    }),
  }),
  z.object({
    trade_item: z.object({
      give_item: z.string().optional(),
      give_gold: z.number().optional(),
      receive_item: z.string().optional(),
      receive_gold: z.number().optional(),
      fail_msg: z.string().optional(),
      success_msg: z.string().optional(),
    }),
  }),
  z.object({
    npc_trade: z.object({
      npc_id: z.string(),
      action: z.enum(["buy", "sell", "stock"]),
      item: z.string().optional(),
      possible_items: z.array(z.string()).optional(),
      cost: z.number().optional(),
      success_msg: z.string().optional(),
      fail_msg: z.string().optional(),
      min_rep: z.number().optional(),
    }),
  }),
  z.object({
    change_reputation: z.object({
      npc_id: z.string(),
      by: z.number(),
    }),
  }),
]);

export type Effect = z.infer<typeof EffectSchema>;

/**
 * Pure function to apply a single effect to a GameState.
 * Returns the brand-new state and the event generated by the transition.
 */
export function applyEffect(
  state: GameState,
  effect: Effect,
  pack?: any
): { state: GameState; event: GameEvent } {
  // Perform a shallow clone of high-level state, deep cloning maps where necessary
  const newState = {
    ...state,
    flags: { ...state.flags },
    vars: { ...state.vars },
    inventory: [...state.inventory],
    objectState: { ...state.objectState },
    journal: [...state.journal],
    visited: { ...state.visited },
    merchantInventories: state.merchantInventories ? JSON.parse(JSON.stringify(state.merchantInventories)) : undefined,
    tradeHistory: state.tradeHistory ? [...state.tradeHistory] : undefined,
    merchantGold: state.merchantGold ? { ...state.merchantGold } : undefined,
    merchantLastRestock: state.merchantLastRestock ? { ...state.merchantLastRestock } : undefined,
    npcRep: state.npcRep ? { ...state.npcRep } : undefined,
  };

  if ("set_flag" in effect) {
    const flag = effect.set_flag;
    newState.flags[flag] = true;
    return {
      state: newState,
      event: { type: "state_change", effect: "set_flag", flag },
    };
  }

  if ("clear_flag" in effect) {
    const flag = effect.clear_flag;
    newState.flags[flag] = false;
    return {
      state: newState,
      event: { type: "state_change", effect: "clear_flag", flag },
    };
  }

  if ("add_item" in effect) {
    const item = effect.add_item;
    if (!newState.inventory.includes(item)) {
      newState.inventory.push(item);
    }
    const currentObj = newState.objectState[item] ?? {};
    newState.objectState[item] = {
      ...currentObj,
      takenBy: "player",
    };
    return {
      state: newState,
      event: { type: "take", item },
    };
  }

  if ("remove_item" in effect) {
    const item = effect.remove_item;
    newState.inventory = newState.inventory.filter((i) => i !== item);
    const currentObj = newState.objectState[item] ?? {};
    newState.objectState[item] = {
      ...currentObj,
      takenBy: "destroyed",
    };
    return {
      state: newState,
      event: { type: "drop", item },
    };
  }

  if ("set_var" in effect) {
    const { name, value } = effect.set_var;
    newState.vars[name] = value;
    return {
      state: newState,
      event: { type: "state_change", effect: "set_var", variable: name, value },
    };
  }

  if ("inc_var" in effect) {
    const { name, by } = effect.inc_var;
    const oldVal = newState.vars[name] ?? 0;
    const newVal = oldVal + by;
    newState.vars[name] = newVal;
    return {
      state: newState,
      event: { type: "state_change", effect: "inc_var", variable: name, value: newVal },
    };
  }

  if ("dec_var" in effect) {
    const { name, by } = effect.dec_var;
    const oldVal = newState.vars[name] ?? 0;
    const newVal = oldVal - by;
    newState.vars[name] = newVal;
    return {
      state: newState,
      event: { type: "state_change", effect: "dec_var", variable: name, value: newVal },
    };
  }

  if ("add_journal" in effect) {
    const text = effect.add_journal;
    newState.journal.push(text);
    return {
      state: newState,
      event: { type: "narration", text },
    };
  }

  if ("goto" in effect) {
    const sceneId = effect.goto;
    newState.current = sceneId;
    newState.visited[sceneId] = true;
    return {
      state: newState,
      event: { type: "state_change", effect: "goto", value: sceneId },
    };
  }

  if ("unlock_exit" in effect) {
    const { from, to } = effect.unlock_exit;
    // We register the unlocked path in flags for simple pure tracking in v1
    newState.flags[`exit_unlocked_${from}_to_${to}`] = true;
    return {
      state: newState,
      event: { type: "unlock_exit", from, to },
    };
  }

  if ("open_object" in effect) {
    const objectId = effect.open_object;
    const currentObj = newState.objectState[objectId] ?? {};
    newState.objectState[objectId] = {
      ...currentObj,
      open: true,
    };
    return {
      state: newState,
      event: { type: "open_object", objectId },
    };
  }

  if ("set_object_locked" in effect) {
    const { id, locked } = effect.set_object_locked;
    const currentObj = newState.objectState[id] ?? {};
    newState.objectState[id] = {
      ...currentObj,
      locked,
    };
    return {
      state: newState,
      event: { type: "state_change", effect: "set_object_locked", flag: id, value: locked },
    };
  }

  if ("narrate" in effect) {
    const text = effect.narrate;
    return {
      state: newState,
      event: { type: "narration", text },
    };
  }

  if ("end_game" in effect) {
    const endingId = effect.end_game;
    newState.ended = true;
    newState.endingId = endingId;
    return {
      state: newState,
      event: { type: "ending", endingId, title: "Game Over", text: "" },
    };
  }

  if ("roll_skill_check" in effect) {
    const { skill, difficulty, success_flag } = effect.roll_skill_check;
    const skillVal = newState.vars[skill] ?? 0;
    const { value: roll, nextSeed } = PureRand.nextInt(newState.seed, 1, 20);
    newState.seed = nextSeed;
    const total = roll + skillVal;
    const success = total >= difficulty;
    newState.flags[success_flag] = success;

    const msg = `[Skill Check] Rolling ${skill.toUpperCase()} (Difficulty: ${difficulty}): Rolled ${roll} + Skill ${skillVal} = ${total}. ` +
                (success ? "🟢 SUCCESS!" : "🔴 FAILURE!");
    newState.journal.push(msg);

    return {
      state: newState,
      event: { type: "narration", text: msg },
    };
  }

  if ("start_combat" in effect) {
    const { npc_id } = effect.start_combat;
    newState.flags[`in_combat_with_${npc_id}`] = true;
    newState.flags[`in_dialogue_with_${npc_id}`] = false;
    return {
      state: newState,
      event: { type: "state_change", effect: "start_combat", flag: npc_id },
    };
  }

  if ("damage_player" in effect) {
    const { by, msg } = effect.damage_player;
    const currentHp = newState.vars["hp"] ?? 20;
    const newHp = Math.max(0, currentHp - by);
    newState.vars["hp"] = newHp;

    const text = msg ?? `You take ${by} damage! (HP: ${newHp}/${newState.vars["max_hp"] ?? 20})`;
    if (newHp <= 0) {
      newState.ended = true;
      newState.endingId = "ending_died_in_combat";
    }

    return {
      state: newState,
      event: { type: "narration", text },
    };
  }

  if ("heal_player" in effect) {
    const { by, msg } = effect.heal_player;
    const currentHp = newState.vars["hp"] ?? 20;
    const maxHp = newState.vars["max_hp"] ?? 20;
    const newHp = Math.min(maxHp, currentHp + by);
    newState.vars["hp"] = newHp;

    const text = msg ?? `You are healed for ${by} points. (HP: ${newHp}/${maxHp})`;

    return {
      state: newState,
      event: { type: "narration", text },
    };
  }

  if ("generate_procedural_room" in effect) {
    const { direction, to_id, name, description, template_id } = effect.generate_procedural_room;
    if (!newState.proceduralRooms) {
      newState.proceduralRooms = [];
    }

    let roomName = name;
    let roomDesc = description;
    let templateObjects: string[] = [];
    let templateNpcs: string[] = [];

    // If template_id is provided, resolve from the pack
    if (template_id && pack && pack.procedural_templates) {
      const template = pack.procedural_templates.find((t: any) => t.id === template_id);
      if (template) {
        // Use seed deterministically to select from name_pool and description_pool
        if (template.name_pool && template.name_pool.length > 0) {
          const { value: selectedName, nextSeed: s1 } = PureRand.choose(newState.seed, template.name_pool) as { value: string; nextSeed: number };
          newState.seed = s1;
          roomName = selectedName;
        }
        if (template.description_pool && template.description_pool.length > 0) {
          const { value: selectedDesc, nextSeed: s2 } = PureRand.choose(newState.seed, template.description_pool) as { value: string; nextSeed: number };
          newState.seed = s2;
          roomDesc = selectedDesc;
        }

        // Deterministically select a possible object to spawn
        if (template.possible_objects && template.possible_objects.length > 0) {
          const { value: objIndex, nextSeed: s3 } = PureRand.nextInt(newState.seed, 0, template.possible_objects.length - 1);
          newState.seed = s3;
          const spawnedObj = template.possible_objects[objIndex];
          if (spawnedObj) {
            templateObjects.push(spawnedObj);
          }
        }

        // Deterministically select a possible NPC to spawn
        if (template.possible_npcs && template.possible_npcs.length > 0) {
          const { value: npcIndex, nextSeed: s4 } = PureRand.nextInt(newState.seed, 0, template.possible_npcs.length - 1);
          newState.seed = s4;
          const spawnedNpc = template.possible_npcs[npcIndex];
          if (spawnedNpc) {
            templateNpcs.push(spawnedNpc);
          }
        }
      }
    }

    // Fallbacks
    roomName = roomName || "Mysterious Chamber";
    roomDesc = roomDesc || "A room generated by mysterious forces.";

    const oppositeDirections: Record<string, string> = {
      north: "south",
      south: "north",
      east: "west",
      west: "east",
      up: "down",
      down: "up",
    };

    const oppDir = oppositeDirections[direction] || "south";

    const exists = newState.proceduralRooms.some((r: any) => r.id === to_id);
    if (!exists) {
      newState.proceduralRooms.push({
        id: to_id,
        name: roomName,
        description: roomDesc,
        objects: templateObjects,
        npcs: templateNpcs,
        exits: [{ direction: oppDir, to: state.current }]
      });
    }

    newState.flags[`procedural_exit_${state.current}_${direction}_to_${to_id}`] = true;

    const msg = `An opening appears to the ${direction}...`;
    return {
      state: newState,
      event: { type: "narration", text: msg },
    };
  }

  if ("trade_item" in effect) {
    const { give_item, give_gold, receive_item, receive_gold, fail_msg, success_msg } = effect.trade_item;

    // 1. Check item requirement
    if (give_item && !newState.inventory.includes(give_item)) {
      const text = fail_msg ?? `You don't have a ${give_item} to trade.`;
      return {
        state,
        event: { type: "narration", text }
      };
    }

    // 2. Check gold requirement
    const currentGold = newState.vars["gold"] ?? 0;
    if (give_gold && currentGold < give_gold) {
      const text = fail_msg ?? `You don't have enough gold (requires ${give_gold} gold, you have ${currentGold}).`;
      return {
        state,
        event: { type: "narration", text }
      };
    }

    // 3. Apply the trade
    if (give_item) {
      newState.inventory = newState.inventory.filter((item) => item !== give_item);
    }
    if (give_gold) {
      newState.vars["gold"] = currentGold - give_gold;
    }
    if (receive_item) {
      newState.inventory.push(receive_item);
    }
    if (receive_gold) {
      const finalGold = (newState.vars["gold"] ?? 0) + receive_gold;
      newState.vars["gold"] = finalGold;
    }

    const text = success_msg ?? `Trade completed successfully!`;
    return {
      state: newState,
      event: { type: "narration", text }
    };
  }

  if ("npc_trade" in effect) {
    const { npc_id, action, item, possible_items, cost, success_msg, fail_msg, min_rep } = effect.npc_trade;

    if (!newState.merchantInventories) {
      newState.merchantInventories = {};
    }
    if (!newState.merchantInventories[npc_id]) {
      newState.merchantInventories[npc_id] = [];
    }
    if (!newState.tradeHistory) {
      newState.tradeHistory = [];
    }

    const npcPack = pack?.npcs?.find((n: any) => n.id === npc_id);

    // Check Reputation requirement
    const repCheck = checkReputationTrade(newState, npcPack, min_rep);
    if (!repCheck.allowed) {
      return {
        state,
        event: { type: "narration", text: fail_msg ?? repCheck.reason ?? "Trade not allowed." }
      };
    }

    if (action === "stock") {
      let stockedItem = item;
      if (!stockedItem && possible_items && possible_items.length > 0) {
        const { value, nextSeed } = PureRand.choose(newState.seed, possible_items);
        newState.seed = nextSeed;
        stockedItem = value;
      }

      if (stockedItem) {
        newState.merchantInventories[npc_id] = [...newState.merchantInventories[npc_id], stockedItem];
        newState.tradeHistory.push({
          step: newState.step,
          npcId: npc_id,
          action: "stock",
          item: stockedItem,
          gold: 0,
        });

        const currentObj = newState.objectState[stockedItem] ?? {};
        newState.objectState[stockedItem] = {
          ...currentObj,
          takenBy: "world",
        };

        const text = success_msg ?? `The merchant stocks: ${stockedItem}.`;
        return {
          state: newState,
          event: { type: "narration", text }
        };
      } else {
        const text = fail_msg ?? `No item was stocked.`;
        return {
          state: newState,
          event: { type: "narration", text }
        };
      }
    }

    if (action === "buy") {
      if (!item) {
        return {
          state,
          event: { type: "narration", text: "No item specified to buy." }
        };
      }
      
      const isStocked = newState.merchantInventories[npc_id].includes(item);
      if (!isStocked) {
        return {
          state,
          event: { type: "narration", text: fail_msg ?? `The merchant does not have ${item} in stock.` }
        };
      }

      const packObj = pack?.objects?.find((o: any) => o.id === item);
      const baseCost = cost ?? packObj?.cost ?? 10;
      const itemCost = calculateTradePrice(newState, npcPack, packObj, baseCost, true);

      const playerGold = newState.vars["gold"] ?? 0;
      if (playerGold < itemCost) {
        return {
          state,
          event: { type: "narration", text: fail_msg ?? `You don't have enough gold (requires ${itemCost} gold, you have ${playerGold}).` }
        };
      }

      // Update Player Gold
      newState.vars["gold"] = playerGold - itemCost;
      
      // Update Merchant Gold
      const mGold = getMerchantGold(newState, npcPack);
      const maxGold = npcPack?.gold_limit;
      newState.merchantGold = newState.merchantGold || {};
      const nextMerchantGold = mGold + itemCost;
      newState.merchantGold[npc_id] = maxGold !== undefined ? Math.min(nextMerchantGold, maxGold) : nextMerchantGold;

      if (!newState.inventory.includes(item)) {
        newState.inventory.push(item);
      }
      newState.merchantInventories[npc_id] = newState.merchantInventories[npc_id].filter((i: string) => i !== item);
      
      const currentObj = newState.objectState[item] ?? {};
      newState.objectState[item] = {
        ...currentObj,
        takenBy: "player",
      };

      newState.tradeHistory.push({
        step: newState.step,
        npcId: npc_id,
        action: "buy",
        item,
        gold: itemCost,
      });

      const text = success_msg ?? `You bought the ${item} for ${itemCost} gold.`;
      return {
        state: newState,
        event: { type: "narration", text }
      };
    }

    if (action === "sell") {
      if (!item) {
        return {
          state,
          event: { type: "narration", text: "No item specified to sell." }
        };
      }

      const hasItem = newState.inventory.includes(item);
      if (!hasItem) {
        return {
          state,
          event: { type: "narration", text: fail_msg ?? `You don't have a ${item} to sell.` }
        };
      }

      const packObj = pack?.objects?.find((o: any) => o.id === item);
      if (packObj?.quest_critical) {
        return {
          state,
          event: { type: "narration", text: fail_msg ?? `You cannot sell quest critical item ${item}.` }
        };
      }

      const baseCost = cost ?? packObj?.cost ?? 10;
      const itemPayout = calculateTradePrice(newState, npcPack, packObj, baseCost, false);

      // Check Merchant Gold
      const mGold = getMerchantGold(newState, npcPack);
      if (mGold < itemPayout) {
        return {
          state,
          event: { type: "narration", text: fail_msg ?? `The merchant does not have enough gold (has ${mGold} gold, requires ${itemPayout}).` }
        };
      }

      // Update Player Gold
      newState.inventory = newState.inventory.filter((i) => i !== item);
      newState.vars["gold"] = (newState.vars["gold"] ?? 0) + itemPayout;
      
      // Update Merchant Gold
      newState.merchantGold = newState.merchantGold || {};
      newState.merchantGold[npc_id] = Math.max(0, mGold - itemPayout);

      newState.merchantInventories[npc_id] = [...newState.merchantInventories[npc_id], item];

      const currentObj = newState.objectState[item] ?? {};
      newState.objectState[item] = {
        ...currentObj,
        takenBy: "world",
      };

      newState.tradeHistory.push({
        step: newState.step,
        npcId: npc_id,
        action: "sell",
        item,
        gold: itemPayout,
      });

      const text = success_msg ?? `You sold the ${item} for ${itemPayout} gold.`;
      return {
        state: newState,
        event: { type: "narration", text }
      };
    }
  }

  if ("change_reputation" in effect) {
    const { npc_id, by } = effect.change_reputation;
    newState.npcRep = newState.npcRep || {};
    const currentRep = newState.npcRep[npc_id] ?? 0;
    const finalRep = currentRep + by;
    newState.npcRep[npc_id] = finalRep;
    return {
      state: newState,
      event: { type: "state_change", effect: "change_reputation", variable: npc_id, value: finalRep }
    };
  }

  throw new Error(`Unknown effect type: ${JSON.stringify(effect)}`);
}

/**
 * Pure function to apply a list of effects to a GameState sequentially.
 */
export function applyEffects(
  state: GameState,
  effects?: Effect[],
  pack?: any
): { state: GameState; events: GameEvent[] } {
  if (!effects || effects.length === 0) {
    return { state, events: [] };
  }

  let currState = state;
  const events: GameEvent[] = [];

  for (const eff of effects) {
    const res = applyEffect(currState, eff, pack);
    currState = res.state;
    events.push(res.event);
  }

  return { state: currState, events };
}
