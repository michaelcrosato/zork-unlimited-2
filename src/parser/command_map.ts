function levenshtein(a: string, b: string): number {
  const tmp: number[][] = [];
  for (let i = 0; i <= a.length; i++) {
    tmp[i] = [i];
  }
  for (let j = 0; j <= b.length; j++) {
    tmp[0][j] = j;
  }
  for (let i = 1; i <= a.length; i++) {
    for (let j = 1; j <= b.length; j++) {
      tmp[i][j] = Math.min(
        tmp[i - 1][j] + 1, // deletion
        tmp[i][j - 1] + 1, // insertion
        tmp[i - 1][j - 1] + (a[i - 1] === b[j - 1] ? 0 : 1) // substitution
      );
    }
  }
  return tmp[a.length][b.length];
}

function isSameAction(act1: Action, act2: Action): boolean {
  if (act1.type !== act2.type) return false;
  const keys1 = Object.keys(act1) as (keyof Action)[];
  const keys2 = Object.keys(act2) as (keyof Action)[];
  if (keys1.length !== keys2.length) return false;
  for (const k of keys1) {
    if ((act1 as any)[k] !== (act2 as any)[k]) return false;
  }
  return true;
}

