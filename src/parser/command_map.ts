function areSynonyms(word1: string, word2: string): boolean {
  const w1 = word1.toLowerCase();
  const w2 = word2.toLowerCase();
  if (w1 === w2) return true;
  for (const group of SYNONYM_GROUPS) {
    if (group.includes(w1) && group.includes(w2)) {
      return true;
    }
  }
  return false;
}

function getNounMatchStrength(userNoun: string, cmdNouns: string[], cmdTokens: string[]): number {
  const noiseWords = ["a", "an", "the", "of", "on", "with", "to", "from", "at", "in", "into", "and", "or"];
  const un = userNoun.toLowerCase();
  if (noiseWords.includes(un)) return 0;

  // 1. Exact match (strongest)
  for (const cn of cmdNouns) {
    if (un === cn.toLowerCase()) return 1.0;
  }
  for (const ct of cmdTokens) {
    const ctLower = ct.toLowerCase();
    if (noiseWords.includes(ctLower)) continue;
    if (un === ctLower) return 1.0;
  }

  // 2. Synonym match
  for (const cn of cmdNouns) {
    if (areSynonyms(un, cn)) return 0.8;
  }
  for (const ct of cmdTokens) {
    const ctLower = ct.toLowerCase();
    if (noiseWords.includes(ctLower)) continue;
    if (areSynonyms(un, ctLower)) return 0.8;
  }

  // 3. Substring match
  if (un.length >= 3) {
    for (const cn of cmdNouns) {
      if (cn.includes(un) || un.includes(cn)) return 0.6;
    }
    for (const ct of cmdTokens) {
      const ctLower = ct.toLowerCase();
      if (noiseWords.includes(ctLower)) continue;
      if (ctLower.includes(un) || un.includes(ctLower)) return 0.6;
    }
  }

  // 4. Levenshtein fuzzy match
  for (const cn of cmdNouns) {
    if (cn.length >= 4) {
      const dist = levenshtein(un, cn);
      const maxDist = cn.length >= 6 ? 2 : 1;
      if (dist <= maxDist) return 0.4;
    }
  }
  for (const ct of cmdTokens) {
    const ctLower = ct.toLowerCase();
    if (noiseWords.includes(ctLower)) continue;
    if (ctLower.length >= 4) {
      const dist = levenshtein(un, ctLower);
      const maxDist = ctLower.length >= 6 ? 2 : 1;
      if (dist <= maxDist) return 0.4;
    }
  }

  return 0;
}

