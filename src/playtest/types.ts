// ─── Turn Log ───────────────────────────────────────────────────────────────

/**
 * A single turn in the playtest trace log.
 *
 * Captures the full context of one player action: what room they were in,
 * what they did, what happened, and why the persona chose that action.
 * The sequence of {@link PlaytestTurnLog} entries forms the complete
 * replayable trace for a session.
 */
export type PlaytestTurnLog = {
  /** 1-indexed step number within the session. */
  step: number;
  /** Identifier of the room the player was in when this action was taken. */
  roomId: string;
  /** The action the persona chose to take (e.g. `"open chest"`, `"go north"`). */
  action: string;
  /** The game's response to the action. */
  observation: string;
  /** Whether the action was rejected by the parser as invalid. */
  wasRejected: boolean;
  /** The persona's reasoning for choosing this action (chain-of-thought). */
  reasonForChoice: string;
};
